// ---------------------------------------------------------------------------
// CLI entry-point
// ---------------------------------------------------------------------------

void main(List<String> arguments) {
  final parser = ArgParser()
    ..addOption(
      'dat',
      abbr: 'd',
      help: 'Path to CF_ENG.DAT archive.',
      mandatory: true,
    )
    ..addOption(
      'output',
      abbr: 'o',
      help: 'Output directory for PNGs.',
      defaultsTo: 'output',
    )
    ..addOption(
      'input',
      abbr: 'i',
      help:
          'Path to pre-extracted directory '
          '(use instead of --dat).',
    )
    ..addOption(
      'sprite-data',
      abbr: 's',
      help:
          'Directory containing exported sprite metadata JSONs '
          '(generated by export_sprite_data.dart).',
    )
    ..addFlag(
      'sprites',
      help: 'Extract individual sprites (requires --sprite-data).',
      negatable: false,
    )
    ..addFlag('help', abbr: 'h', negatable: false, help: 'Show usage.');

  final args = parser.parse(arguments);
  if (args['help'] as bool) {
    print('Usage: dart run tool/sprites/main.dart [options]');
    print(parser.usage);
    return;
  }

  final outputDir = Directory(args['output'] as String);
  if (!outputDir.existsSync()) {
    outputDir.createSync(recursive: true);
  }

  // Resolve file access — archive or pre-extracted directory.
  // TODO(bramp): Instead of these two closures, we could define an abstract
  // ArchiveProvider class with two implementations.
  final Uint8List Function(String) getFile;
  final bool Function(String) hasFile;

  final extractedPath = args['input'] as String?;
  if (extractedPath != null) {
    final dir = Directory(extractedPath);
    if (!dir.existsSync()) {
      print('Error: directory not found: $extractedPath');
      return;
    }
    getFile = (f) => File(p.join(dir.path, f)).readAsBytesSync();
    hasFile = (f) => File(p.join(dir.path, f)).existsSync();
  } else {
    final datFile = File(args['dat'] as String);
    if (!datFile.existsSync()) {
      print('Error: DAT not found: ${datFile.path}');
      return;
    }
    final reader = DatReader(datFile)..read();
    final entryMap = {for (final e in reader.entries) e.filename: e};
    getFile = (f) => reader.getFileBytes(entryMap[f]!);
    hasFile = entryMap.containsKey;
  }

  // Load sprite metadata early so copt PNG exports can use per-sprite
  // palette correction. This must NOT be gated on --sprites; the copt
  // sheet export always needs the frame data for correct palette fix-up.
  final spriteDataPath = args['sprite-data'] as String?;
  List<SpriteSheetType>? sheetTypes;
  List<SpriteFrame>? coptFrames;

  if (spriteDataPath != null) {
    sheetTypes = _loadSpriteSheetTypes(spriteDataPath);
    if (sheetTypes != null) {
      coptFrames = sheetTypes
          .expand((st) => st.entries.expand((g) => g))
          .where((f) => f.gfxType == GfxType.inGame2)
          .toList();
    }
  }

  var exported = 0;

  // --- 4-bit sprite sheets ---
  for (final entry in _spriteSheets4Bit.entries) {
    if (!hasFile(entry.key)) continue;
    exported += _export4Bit(
      entry.key,
      getFile(entry.key),
      entry.value,
      outputDir,
      label: '4-bit',
    );
  }

  // --- Army sheets (4-bit) ---
  for (final entry in _armySheets.entries) {
    if (!hasFile(entry.key)) continue;
    exported += _export4Bit(
      entry.key,
      getFile(entry.key),
      [entry.value],
      outputDir,
      label: '4-bit army',
    );
  }

  // --- Copt sheets (4-bit, dual palette) ---
  if (coptFrames == null) {
    final coptFiles = _coptSheets.keys.where(hasFile).toList();
    if (coptFiles.isNotEmpty) {
      print(
        '  Skipping ${coptFiles.length} copt sheet(s) '
        '(${coptFiles.join(", ")}): --sprite-data is required for '
        'correct multi-palette decoding.',
      );
    }
  } else {
    for (final entry in _coptSheets.entries) {
      if (!hasFile(entry.key)) continue;
      exported += _export4Bit(
        entry.key,
        getFile(entry.key),
        entry.value,
        outputDir,
        label: '4-bit copt',
        spriteFrames: coptFrames,
      );
    }
  }

  // --- 8-bit linear images ---
  for (final entry in _linear8Bit.entries) {
    if (!hasFile(entry.key)) continue;
    exported += _export8BitLinear(
      entry.key,
      getFile(entry.key),
      entry.value,
      outputDir,
    );
  }

  // --- Planar Mode X images ---
  for (final entry in _planarModeX.entries) {
    if (!hasFile(entry.key)) continue;
    exported += _exportPlanarModeX(
      entry.key,
      getFile(entry.key),
      entry.value,
      outputDir,
    );
  }

  // --- Tile base blocks ---
  for (final filename in _tileBaseBlocks) {
    if (!hasFile(filename)) continue;
    exported += _exportTile(
      filename,
      getFile(filename),
      null,
      outputDir,
      label: 'tile base',
    );
  }

  // --- Tile sub blocks ---
  for (final filename in _tileSubBlocks) {
    if (!hasFile(filename)) continue;
    final prefix = filename.substring(0, 3);
    final baseFilename = '${prefix}base.blk';
    if (!hasFile(baseFilename)) continue;
    exported += _exportTile(
      filename,
      getFile(filename),
      getFile(baseFilename),
      outputDir,
      label: 'tile sub',
    );
  }

  print('Exported $exported images to ${outputDir.path}');

  // --- Individual sprite extraction ---
  if (args['sprites'] as bool) {
    if (spriteDataPath == null) {
      print('Error: --sprite-data is required when using --sprites.');
      return;
    }
    if (sheetTypes == null) {
      print('Error: failed to load sprite data from $spriteDataPath');
      return;
    }

    _exportSpriteAtlases(
      sheetTypes: sheetTypes,
      hasFile: hasFile,
      outputDir: outputDir,
    );
  }
}

