/// Maps [GfxType] values to their source .dat filename and palette specs.
///
/// Terrain-specific types (eGFX_IN_GAME, eGFX_IN_GAME2) are handled
/// dynamically since the filename depends on the active tileset.
const _gfxTypeFiles = <GfxType, _GfxFileSpec>{
  GfxType.font: _GfxFileSpec('font.dat', [_PaletteSpec(0xA000, 0x10, 0xD0)]),
  GfxType.pstuff: _GfxFileSpec('pstuff.dat', [
    _PaletteSpec(0xA000, 0x10, 0xF0),
  ]),
  GfxType.hill: _GfxFileSpec('hillbits.dat', [
    _PaletteSpec(0x6900, 0x10, 0xB0),
  ]),
  GfxType.recruit: _GfxFileSpec('hillbits.dat', [
    _PaletteSpec(0x6900, 0x10, 0xB0),
  ]),
  GfxType.briefing: _GfxFileSpec('pstuff.dat', [
    _PaletteSpec(0xA000, 0x10, 0xF0),
  ]),
  GfxType.rankFont: _GfxFileSpec('rankfont.dat', [
    _PaletteSpec(0xA000, 0x80, 0x40),
  ]),
  GfxType.service: _GfxFileSpec('morphbig.dat', [
    _PaletteSpec(0xFA00, 0x40, 0x00),
  ]),
};

class _GfxFileSpec {
  const _GfxFileSpec(this.filename, this.paletteSpecs);
  final String filename;
  final List<_PaletteSpec> paletteSpecs;
}

/// Loads all [SpriteSheetType] definitions from a directory of
/// `sprite_sheet_*.json` files.
///
/// Returns `null` if the path is invalid or no data is found.
List<SpriteSheetType>? _loadSpriteSheetTypes(String spriteDataPath) {
  if (!FileSystemEntity.isDirectorySync(spriteDataPath)) {
    print(
      '\nError: --sprite-data must be a directory containing '
      'sprite_sheet_*.json files.',
    );
    print('Run tool/sprites/export_sprite_data.dart first.');
    return null;
  }

  print('\nLoading sprite data from JSON files in $spriteDataPath...');
  final sheetTypes = <SpriteSheetType>[];
  final dir = Directory(spriteDataPath);
  for (final file in dir.listSync().whereType<File>()) {
    if (!p.basename(file.path).endsWith('.json')) continue;
    if (!p.basename(file.path).startsWith('sprite_sheet_')) continue;

    final nameMatch = RegExp(
      r'sprite_sheet_(.+)\.json',
    ).firstMatch(p.basename(file.path));
    if (nameMatch == null) continue;

    final name = nameMatch.group(1)!;
    final json = jsonDecode(file.readAsStringSync()) as List<dynamic>;
    sheetTypes.add(SpriteSheetType.fromJson(name, json));
  }

  if (sheetTypes.isEmpty) {
    print('No sprite sheet metadata found in $spriteDataPath');
    return null;
  }

  return sheetTypes;
}

/// Generates TexturePacker JSON atlas files alongside the already-exported
/// sprite sheet PNGs.
///
/// For each source .dat file referenced by sprites, a companion `.json` is
/// written next to the existing `.png`. Sprite names follow the convention
/// `{SpriteSheetType}/{groupHex}_{frameIndex}`.
void _exportSpriteAtlases({
  required List<SpriteSheetType> sheetTypes,
  required bool Function(String) hasFile,
  required Directory outputDir,
}) {
  print('Found ${sheetTypes.length} sprite sheet types:');
  for (final st in sheetTypes) {
    var totalFrames = 0;
    for (final group in st.entries) {
      totalFrames += group.length;
    }
    print(
      '  ${st.name}: ${st.entries.length} groups, $totalFrames total frames',
    );
  }

  // Resolve GfxType → actual filename (for terrain-specific types, pick the
  // first available file).
  final gfxTypeFilename = <GfxType, String>{};
  for (final entry in _gfxTypeFiles.entries) {
    if (hasFile(entry.value.filename)) {
      gfxTypeFilename[entry.key] = entry.value.filename;
    }
  }

  // Pick first available army/copt filename for terrain-specific types.
  for (final entry in _armySheets.entries) {
    if (hasFile(entry.key)) {
      gfxTypeFilename[GfxType.inGame] = entry.key;
      break;
    }
  }
  for (final entry in _coptSheets.entries) {
    if (hasFile(entry.key)) {
      gfxTypeFilename[GfxType.inGame2] = entry.key;
      break;
    }
  }

  // Collect atlas entries grouped by source .dat filename.
  // key = .dat filenameList, value = list of atlas entries.
  final atlasEntries = <String, List<AtlasEntry>>{};

  // Also track image heights per filename.
  final imageHeights = <String, int>{};

  // Build a map of GfxType to ALL files that use it.
  final gfxTypeToFiles = <GfxType, List<String>>{};
  for (final entry in _gfxTypeFiles.entries) {
    if (hasFile(entry.value.filename)) {
      gfxTypeToFiles.putIfAbsent(entry.key, () => []).add(entry.value.filename);
    }
  }
  for (final entry in _armySheets.entries) {
    if (hasFile(entry.key)) {
      gfxTypeToFiles.putIfAbsent(GfxType.inGame, () => []).add(entry.key);
    }
  }
  for (final entry in _coptSheets.entries) {
    if (hasFile(entry.key)) {
      gfxTypeToFiles.putIfAbsent(GfxType.inGame2, () => []).add(entry.key);
    }
  }

  for (final sheetType in sheetTypes) {
    for (var groupIdx = 0; groupIdx < sheetType.entries.length; groupIdx++) {
      final group = sheetType.entries[groupIdx];
      for (var frameIdx = 0; frameIdx < group.length; frameIdx++) {
        final frame = group[frameIdx];

        if (frame.width <= 0 || frame.height <= 0) continue;

        final filenames = gfxTypeToFiles[frame.gfxType];
        if (filenames == null) continue;

        for (final filename in filenames) {
          final groupLabel =
              spriteGroupName(
                sheetTypeName: sheetType.name,
                groupIndex: groupIdx,
              ) ??
              groupIdx.toRadixString(16).padLeft(2, '0');
          final name = '${sheetType.name}/${groupLabel}_$frameIdx';

          final x = frame.pixelX();
          final y = frame.pixelY();

          atlasEntries
              .putIfAbsent(filename, () => [])
              .add(
                AtlasEntry(
                  name: name,
                  x: x,
                  y: y,
                  width: frame.width,
                  height: frame.height,
                  anchorX: frame.modX,
                  anchorY: frame.modY,
                ),
              );

          // Track maximum required height for this image.
          final bottomEdge = y + frame.height;
          final prev = imageHeights[filename] ?? 0;
          if (bottomEdge > prev) {
            imageHeights[filename] = bottomEdge;
          }
        }
      }
    }
  }

  // Write one TexturePacker JSON per source file.
  var atlasCount = 0;
  for (final entry in atlasEntries.entries) {
    final datFilename = entry.key;
    final entries = entry.value;
    final pngFilename = '${p.basenameWithoutExtension(datFilename)}.png';
    final jsonFilename = '${p.basenameWithoutExtension(datFilename)}.json';

    const imageWidth = 320;
    final imageHeight = imageHeights[datFilename] ?? 0;

    final json = generateAtlasJson(
      imageFilename: pngFilename,
      imageWidth: imageWidth,
      imageHeight: imageHeight,
      entries: entries,
    );

    final outPath = p.join(outputDir.path, jsonFilename);
    File(outPath).writeAsStringSync(json);
    print('  $outPath (${entries.length} sprites)');
    atlasCount++;
  }

  print('Generated $atlasCount atlas JSON files in ${outputDir.path}');
}
